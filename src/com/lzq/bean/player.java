package com.lzq.bean;

public class player {

	private String name;
	private String stature;
	private String bodyweight;
	private String location;
	private String playernumber;
	private String averagescorepergame;
	private int ans;
	
	public int getAns() {
		return ans;
	}
	public void setAns(int ans) {
		this.ans = ans;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStature() {
		return stature;
	}
	public void setStature(String stature) {
		this.stature = stature;
	}
	public String getBodyweight() {
		return bodyweight;
	}
	public void setBodyweight(String bodyweight) {
		this.bodyweight = bodyweight;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getPlayernumber() {
		return playernumber;
	}
	public void setPlayernumber(String playernumber) {
		this.playernumber = playernumber;
	}
	public String getAveragescorepergame() {
		return averagescorepergame;
	}
	public void setAveragescorepergame(String averagescorepergame) {
		this.averagescorepergame = averagescorepergame;
	}
	
	
}
